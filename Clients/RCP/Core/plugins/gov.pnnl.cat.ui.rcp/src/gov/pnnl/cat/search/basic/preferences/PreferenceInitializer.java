package gov.pnnl.cat.search.basic.preferences;

import gov.pnnl.cat.ui.rcp.CatRcpPlugin;

import org.eclipse.core.runtime.preferences.AbstractPreferenceInitializer;
import org.eclipse.jface.dialogs.MessageDialogWithToggle;
import org.eclipse.jface.preference.IPreferenceStore;

/**
 * Class used to initialize default preference values.
 * @version $Revision: 1.0 $
 */
public class PreferenceInitializer extends AbstractPreferenceInitializer {
  public PreferenceInitializer() {
  }

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.runtime.preferences.AbstractPreferenceInitializer#initializeDefaultPreferences()
	 */
	public void initializeDefaultPreferences() {
		IPreferenceStore store = CatRcpPlugin.getDefault().getPreferenceStore();
//    System.out.println("setting default to PROMPT");
		store.setDefault(IPreferenceConstants.P_SWITCH_PERSPECTIVES, MessageDialogWithToggle.PROMPT);
	}

}
